    private void btn_detailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detailActionPerformed
        // TODO add your handling code here:
        int row = datatable.getSelectedRow();
        String detail_id = datatable.getModel().getValueAt(row, 0).toString();
        
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Connection connection = DriverManager.getConnection("jdbc:ucanaccess://C:\\Users\\ASUS\\Documents\\NetBeansProjects\\UAS\\kurir.accdb");
            
            String sql = "SELECT * FROM kurir Where id = " + detail_id;
            Statement statement = connection.createStatement();
            ResultSet result = statement.executeQuery(sql);
            
            while(result.next()) {
                String message = "";
                message += "================\n";
                message += "Detail Data\n";
                message += "================\n";
                message += "Id : " + String.valueOf(result.getInt("ID")) + "\n";
                message += "Nama Barang : " + result.getString("nama_barang") + "\n";
                message += "Nama Pengirim: " + result.getString("nama_pengirim") + "\n";
                message += "Nama Penerima : " + result.getString("nama_penerima") + "\n";
                message += "No Telp : " + result.getString("no_telp") + "\n";
                message += "Alamat : " + result.getString("alamat") + "\n";
                message += "Berat : " + String.valueOf(result.getString("berat")) + "\n";
                message += "Jarak : " + String.valueOf(result.getString("jarak")) + "\n";
                message += "Jenis Pengiriman : " + result.getString("jenis_pengiriman") + "\n";
                message += "Harga Ongkir : " + String.valueOf(result.getString("harga_ongkir")) + "\n";
                message += "Pajak : " + String.valueOf(result.getString("pajak")) + "\n";
                message += "Total Ongkir : " + String.valueOf(result.getString("total_ongkir")) + "\n";
                
                JOptionPane.showMessageDialog(null, message,"Informaton",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_detailActionPerformed

