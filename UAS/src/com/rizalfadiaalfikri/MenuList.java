    public void refreshData(){
        DefaultTableModel tableModel = (DefaultTableModel) datatable.getModel();
        
        tableModel.setRowCount(0);
        tableModel.setColumnCount(0);
        
        tableModel.addColumn("id");
        tableModel.addColumn("Nama Barang");
        tableModel.addColumn("Nama Pengirim");
        tableModel.addColumn("Nama Penerima");
        tableModel.addColumn("Alamat");
        tableModel.addColumn("Berat");
        tableModel.addColumn("Jarak");
        tableModel.addColumn("Total Ongkir");
        
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Connection connection = DriverManager.getConnection("jdbc:ucanaccess://C:\\Users\\ASUS\\Documents\\NetBeansProjects\\UAS\\kurir.accdb");
            
            String sql = "SELECT * FROM kurir";
            Statement statement = connection.createStatement();
            ResultSet result = statement.executeQuery(sql);
            
            while(result.next()) {
                String id = String.valueOf(result.getInt("ID"));
                String nama_barang = result.getString("nama_barang");
                String nama_pengirim = result.getString("nama_pengirim");
                String nama_penerima = result.getString("nama_penerima");
                String alamat = result.getString("alamat");
                String berat = String.valueOf(result.getString("berat"));
                String jarak = String.valueOf(result.getString("jarak"));
                String total_ongkir = String.valueOf(result.getString("total_ongkir"));
                
                String[] row = {id, nama_barang, nama_pengirim, nama_penerima, alamat, berat, jarak, total_ongkir};
                tableModel.addRow(row);
            }
                
        }catch (Exception e) {
            System.out.println(e);
        }
    }    

